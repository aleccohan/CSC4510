#include "general.h"

/* expr
   Parses strings in the language generated by the rule:
   <expr> -> <term> {(+ | -) <term>}
 */
int expr() 
{
   printf("Enter <expr>\n");

   int right,left,operation;

   /* Parse the first term */
   left = term();
   //left = number;
   cout << " IN EXPR left is = " << left << endl;

   /* As long as the next token is + or -, get
      the next token and parse the next term */
   while (nextToken == ADD_OP || nextToken == SUB_OP) {
      operation = nextToken;
      lex();
      right=term();
      cout << " IN EXPR right is = " << right << endl;
      if(operation == ADD_OP)
         left+=right;
      else if(operation == SUB_OP)
         left-=right;
      else
         error("expected - or +");
   }
   printf("Exit <expr>\n");

   cout << endl;
   cout << "exiting expr RETURNING " << left << endl;
   return left;
} /* End of function expr */


/* term
 *  Parses strings in the language generated by the rule:
 *  <term> -> <factor> {(* | /) <factor>)
 */
int term() 
{
   printf("Enter <term>\n");
   /* Parse the first factor */
   
   int right,left,operation;

   left=factor();
   /* As long as the next token is * or /, get the
      next token and parse the next factor */
   while (nextToken == MULT_OP || nextToken == DIV_OP) {
      operation = nextToken;
      lex();
      right=factor();
      if(operation == MULT_OP)
         left*=right;
      else if(operation == DIV_OP)
         left/=right;
      else
         error("expected * or /");
   }
   printf("Exit <term>\n");

   cout << endl;
   cout << "exiting term RETURNING " << left << endl;
   return left;
} /* End of function term */


/* factor
 * Parses strings in the language generated by the rule:
 * 
 *   <factor> -> id | int_constant | ( <expr  )
 *
 */
int factor() 
{
   printf("Enter <factor>\n");
   int val;
   /* Determine which RHS */
   if (nextToken == IDENT || nextToken == INT_LIT){
      /* Get the next token */
      val = number;
      lex();
   }
   /* If the RHS is ( <expr> ), call lex to pass over the left 
      parenthesis, call expr and check for the right parenthesis */
   else if (nextToken == LEFT_PAREN) {
         lex();
         val = expr();
         if (nextToken == RIGHT_PAREN)
            lex();
         else
            error("left without right paren");
   } else {
   /* It was not an id, an integer literal, or a left
       parenthesis */
       error("expected an id, integer, or a left paren");
   } /* End of else */
   printf("Exit <factor>\n");
   cout << endl;
   cout << "exiting factor RETURNING " << val << endl;
   return val;
}/* End of function factor */

